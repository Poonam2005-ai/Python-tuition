# Positive Exponents
# Example 1 : 2^3  --> 2 ^ (2 * 2 * 2) 
# Note -> in programming if we want to take a power of 
# something then we have to use exponentiation operator 
print(2**3) 

# 2 * 2 * 2
# 4 * 2
# 8  --> 2**3

# example 2
print(25**2)

#output --> 625

#------------------------------------------------------

# Negative Exponents
# if this is positive then ---> 2 ^ (2 * 2)  but when it is negative then ---> we need to take the (2 * 2) at denominator 
# 2 ^ (1/(2 * 2))

print(2**-2)

# 2 ^ 1/(2 * 2)
#2 ^ 1/4
#1/4 --> 0.25  --> one fourth part 
#2 ^ 0.25

# output -> 0.25

#------------------------------------------------------
# Fractional Exponents
print(16 ** 0.5) 

#16 ^ 0.5
#4 ^ (4) # 4 * 4
#16 ^ 1/2
#4.0 

