# floor --> if we want to take only integer value from the given output 
# that time we calculate using floor division 

# 10 / 3 --> 3.33333333
print(10//3)

# Negative numbers
# we have to take lesser integer value than the output in case of of negative numbers
print(-10//3) # output -> -3.333 = -4

# Example 3
# Mixed numbers
print(10//-3) # output -> -3.33333, floor value -> -4 

